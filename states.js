function loadLGAs() {
    const lgaSelect = document.getElementById("lga");
    const wardSelect = document.getElementById("ward");
    const pollingUnitSelect = document.getElementById("polling-unit");

    // Populate LGAs for Anambra
    lgaSelect.innerHTML =
        "<option value='' selected disabled>Select LGA</option>";
    for (const lga in data["Anambra"]) {
        const option = document.createElement("option");
        option.value = lga;
        option.text = lga;
        lgaSelect.add(option);
    }

    // Reset dependent dropdowns
    wardSelect.innerHTML =
        "<option value='' selected disabled>Select LGA first</option>";
    pollingUnitSelect.innerHTML =
        "<option value='' selected disabled>Select Ward first</option>";

    wardSelect.disabled = true;
    pollingUnitSelect.disabled = true;
}

function loadWards() {
    const lgaSelect = document.getElementById("lga");
    const wardSelect = document.getElementById("ward");
    const pollingUnitSelect = document.getElementById("polling-unit");
    const selectedLGA = lgaSelect.value;

    // Clear dependent dropdowns
    wardSelect.innerHTML =
        "<option value='' selected disabled>Select Ward</option>";
    pollingUnitSelect.innerHTML =
        "<option value='' selected disabled>Select Ward first</option>";

    if (selectedLGA) {
        wardSelect.disabled = false;
        for (const ward in data["Anambra"][selectedLGA]) {
            const option = document.createElement("option");
            option.value = ward;
            option.text = ward;
            wardSelect.add(option);
        }
    }
    pollingUnitSelect.disabled = true;
}

function loadPollingUnits() {
    const lgaSelect = document.getElementById("lga");
    const wardSelect = document.getElementById("ward");
    const pollingUnitSelect = document.getElementById("polling-unit");
    const selectedLGA = lgaSelect.value;
    const selectedWard = wardSelect.value;

    pollingUnitSelect.innerHTML =
        "<option value='' selected disabled>Select Polling Unit</option>";

    if (selectedWard) {
        pollingUnitSelect.disabled = false;
        for (const unit of data["Anambra"][selectedLGA][selectedWard]) {
            const option = document.createElement("option");
            option.value = unit;
            option.text = unit;
            pollingUnitSelect.add(option);
        }
    }
}
